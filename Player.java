import java.util.Scanner;

public class Player extends Character
{

/////////////////////////////////////////// ATTRIBUTES ///////////////////////////////////////////
	
//////////////////////////////////////////CONSTRUCTORS //////////////////////////////////////////
	Player(Scanner stream, double gameVersion)
	{
		super(stream, gameVersion);
		nextMove = new UI();
	}
	
	
	Player(int id, String n, String desc)
	{
		super(id, n, desc);
		nextMove = new UI();

	}
	
//////////////////////////////////////// MEMBER FUNCTIONS ////////////////////////////////////////
//--------------------------------------------------------------------------------------------
	public void makeMove()
	{

		Move action = nextMove.getMove(this, here);
		action.execute();
		placeID = here.getID();
	}
}// End public class Player
