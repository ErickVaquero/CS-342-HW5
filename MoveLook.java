public class MoveLook implements Move
{
/////////////////////////////////////////// ATTRIBUTES ///////////////////////////////////////////
	private Character character;
	private Place place;
	
//////////////////////////////////////////CONSTRUCTOR //////////////////////////////////////////
	MoveLook(Character ch, Place p)
	{
		place = p;
		character = ch;
	}
	
////////////////////////////////////////MEMBER FUNCTIONS ////////////////////////////////////////	
//--------------------------------------------------------------------------------------------
	@Override
	public void execute()
	{
		//System.out.println(CleanLineScanner.divider1);
		
		character.display(character.name() + " takes a moment to focus " +
		"and re-examines the " + place.name() + "\n");
		character.display(place.look());
	}

}// End of public class MoveLook implements Move
