public class MoveInve implements Move
{
/////////////////////////////////////////// ATTRIBUTES ///////////////////////////////////////////
	private Character character;
	private Place place;
	
//////////////////////////////////////////CONSTRUCTOR //////////////////////////////////////////
	MoveInve(Character ch, Place p)
	{
		character = ch;
		place = p;
	}
	
////////////////////////////////////////MEMBER FUNCTIONS ////////////////////////////////////////	
//--------------------------------------------------------------------------------------------
	@Override
	public void execute()
	{
		//System.out.println(CleanLineScanner.divider1);
		
		character.display(character.name() + " finds a spot in the " +
		place.name() + " to sit and look in their inventory:");
		character.showInventory();
	}

}// End of public class MoveInve implements Move
