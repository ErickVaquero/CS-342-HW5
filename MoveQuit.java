public class MoveQuit implements Move
{
/////////////////////////////////////////// ATTRIBUTES ///////////////////////////////////////////
	private Character character;
	private Place place;
	
//////////////////////////////////////////CONSTRUCTOR //////////////////////////////////////////
	MoveQuit(Character ch, Place p)
	{
		character = ch;
		place = p;
	}
	
////////////////////////////////////////MEMBER FUNCTIONS ////////////////////////////////////////	
//--------------------------------------------------------------------------------------------
	@Override
	public void execute()
	{
		//System.out.println(CleanLineScanner.divider1);
		
		place.removeCharacter(character);
		character.display(character.name() + " left the map."); 
		System.exit(0);
	}

}// End of public class MoveQuit implements Move
